def count_neighbors(grid, x, y):
    """Count live neighbors for a cell at position (x, y) with wrapping."""
    width = len(grid[0])
    height = len(grid)
    count = 0
    
    # Check all 8 neighboring cells (with toroidal/wrapping edges)
    for dy in [-1, 0, 1]:
        for dx in [-1, 0, 1]:
            if dx == 0 and dy == 0:
                continue
            
            # Wrap around edges (toroidal grid)
            nx = (x + dx) % width
            ny = (y + dy) % height
            count += grid[ny][nx]
    
    return count

def compute_next_generation(grid):
    """Compute the next generation of the game."""
    height = len(grid)
    width = len(grid[0])
    new_grid = [[0 for _ in range(width)] for _ in range(height)]
    
    for y in range(height):
        for x in range(width):
            neighbors = count_neighbors(grid, x, y)
            
            # Apply Game of Life rules
            if grid[y][x] == 1:
                # Cell is alive - check survival rules
                if neighbors in SURVIVAL_RULES:
                    new_grid[y][x] = 1
            else:
                # Cell is dead - check birth rules
                if neighbors in BIRTH_RULE:
                    new_grid[y][x] = 1
    
    return new_grid

def count_live_cells(grid):
    """Count the total number of live cells."""
    return sum(sum(row) for row in grid)

